/**
 * Generic Admin Property is a state representing a device property of an
 * element that can be read or written
 */
typedef struct st_rm_mesh_generic_admin_prop_srv_info
{
    /**
     * Admin Property ID field is a 2-octet Assigned Number value referencing a
     * device property
     */
    uint16_t property_id;

    /**
     * Admin User Access field is an enumeration indicating whether the device
     * property can be read or written as a Generic User Property
     */
    uint8_t user_access;

    /** Admin Property Value field is a conditional field */
    uint8_t * property_value;
    uint16_t  property_value_len;
} rm_mesh_generic_admin_prop_srv_info_t;

/** BLE mesh generic admin prop instance control block. DO NOT INITIALIZE. Initialization occurs when RM_MESH_GENERIC_ADMIN_PROP_SRV_Open() is called. */
typedef struct st_rm_mesh_generic_admin_prop_srv_instance_ctrl
{
    uint32_t open;                                // To check whether api has been opened or not.
    rm_ble_mesh_access_model_handle_t model_handle;
    rm_ble_mesh_access_model_handle_t setup_server_handle;

    rm_ble_mesh_model_server_cfg_t const * p_cfg; // Pointer to initial configurations.
} rm_mesh_generic_admin_prop_srv_instance_ctrl_t;

